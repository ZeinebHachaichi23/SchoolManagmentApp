    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         Statement st;
        String sql="select * from enseignant where id='"+id.getText()+"'";
        
        try {
            st = conn.getConnection().createStatement();
            ResultSet rs1= st.executeQuery(sql);
            while(rs1.next()){
            nomTxt.setText(rs1.getString("Nom"));
            prenomTxt.setText(rs1.getString("Prenom"));
            matiereTxt.setText(rs1.getString("matiere"));
            idTxt.setText(rs1.getString("id"));
            usernameTxt.setText(rs1.getString("username"));
            pwdTxt.setText(rs1.getString("password"));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(interfaceadmin1.class.getName()).log(Level.SEVERE, null, "Aucun √©tudient trouv√©");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

